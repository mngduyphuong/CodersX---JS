function calculateSquareArea(a)
{
    return a*a;
}

function calculateSquarePerimeter(a)
{
    return a+a;
}

function calculateRectangleArea(a,h)
{
    return a*h;
}

function calculateRectanglePerimeter(a,h)
{
    return a*h/2;
}

function calculateCirlceArea(r)
{
    return 3.14*r*r;
}

function calculateCirlcePerimeter(r)
{
    return 2*3.14*r;
}